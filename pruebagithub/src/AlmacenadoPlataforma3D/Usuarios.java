   void MostrarDatos(String valor){
       
       //Restricciones
        RestrictedTextField restricted = new RestrictedTextField(txt2);// campo nombre solo valores de letras
        restricted.setOnlyText(rootPaneCheckingEnabled);
        DefaultTableModel modelo= new DefaultTableModel();
        
        RestrictedTextField restricteds = new RestrictedTextField(txt1);// campo apellido solo valores de letras
        restricteds.setOnlyText(rootPaneCheckingEnabled);
       
        //A continuacion se crea los campos a mostar en la Jtable1
    modelo.addColumn("Rut");
    modelo.addColumn("Nombre");
    modelo.addColumn("Apellido");
    modelo.addColumn("F. Nacimiento");
    modelo.addColumn("Pass");
    modelo.addColumn("Cargo");
      jTable1.setModel(modelo);
      //Crear
      String sql="";
      if(valor.equals("")){sql="select * from usuario";}
      else{ sql="select * from usuario where rut='"+valor+"'";}
      
   String datos[] = new String [6];
    try{
         Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                datos[0]=rs.getString(1);               
                datos[1]=rs.getString(4);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(6);
                datos[4]=rs.getString(2);
                datos[5]=rs.getString(5);               
                modelo.addRow(datos);
            }
            jTable1.setModel(modelo);
            
      }catch(SQLException ex){Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);}       
   }

